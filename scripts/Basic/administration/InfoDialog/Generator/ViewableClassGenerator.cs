		}

		/// <summary>While loading this class, make some registrations in the ClassManager for handling ViewableClasses</summary>
		public static void Bootstrap() {
			//register the CheckViewabilityClass method so every ClassManager managed Type will be
			//checked here for its Viewability...
			if (!Globals.FastStartUp) {
				ClassManager.RegisterSupplySubclasses<object>(CheckViewabilityClass);
			}
		}

		/// <summary>Check if the given type is in the viewableSpecials array</summary>
		internal static bool IsViewableSpecial(Type type) {
			foreach (var specType in viewableSpecials) {
				if (specType.IsAssignableFrom(type)) {//specialViewable type is some kind of parent fo the checked type
					return true;
				}
			}
			return false;
		}

		/// <summary>
		/// Method for checking if the given Type is Viewable. If so, put it to the list.
		/// Used as hooked delegate in ClassManager
		/// </summary>
		public static bool CheckViewabilityClass(Type type) {
			//look if the type has this attribute, don't look to parent classes 
			//(if the type has not the attribute but some parent has, we dont care - if we want
			//it to be infoized, we must add a ViewableClass attribute to it)
			if (Attribute.IsDefined(type, typeof(ViewableClassAttribute), false) ||
							IsViewableSpecial(type)) {
				TryAddAsViewable(type);
			} else if (Attribute.IsDefined(type, typeof(ViewDescriptorAttribute), false)) {
				var descHandledType = ((ViewDescriptorAttribute) type.GetCustomAttributes(typeof(ViewDescriptorAttribute), false)[0]).HandledType;
				TryAddAsViewable(descHandledType);
				List<Type> descriptors;
				if (viewableDescriptorsForTypes.TryGetValue(descHandledType, out descriptors)) {
					descriptors.Add(type); //new descriptor to the list
				} else {
					descriptors = new List<Type>();
					descriptors.Add(type);
					viewableDescriptorsForTypes.Add(descHandledType, descriptors); //we have found some descriptor class
				}
			}
			return false;
		}

		private static void TryAddAsViewable(Type type) {
			if (!viewableClasses.Contains(type)) {
				viewableClasses.Add(type);
			}
		}

		/// <summary>Go through all descriptors and look if their handled type is assignable from the just generated one</summary>
		private static List<Type> FindDescriptorsForType(Type infoizedType) {
			var typeList = new List<Type>();
			var retList = new List<Type>();
			foreach (var describedType in viewableDescriptorsForTypes.Keys) {
				if (describedType.IsAssignableFrom(infoizedType)) {
					//a describedType is some parent class of the infoized type
					typeList.Add(describedType);//the described types list will be sorted and then used for getting and generating all descriptors
				}
			}
			typeList.Sort(TypeHierarchyComparer.instance);
			//now take the types that are assignable from the infoized type and get their descriptors in the right order
			foreach (var assignableTypeWithDescriptorList in typeList) {
				retList.AddRange(viewableDescriptorsForTypes[assignableTypeWithDescriptorList]);
			}
			return retList;
		}

		public static PropertyInfo GetPropertyBaseDefinition(PropertyInfo propertyInfo) {
			var method = propertyInfo.GetAccessors(true)[0];
			if (method == null)
				return null;

			var baseMethod = MemberWrapper.GetMIBaseDefinition(method);

			//if (baseMethod.ReflectedType == propertyInfo.ReflectedType)
			//    return propertyInfo;

			var allProperties = BindingFlags.Instance | BindingFlags.Public
				| BindingFlags.NonPublic | BindingFlags.Static;

			var parInfAr = propertyInfo.GetIndexParameters();
			var parTypAr = new Type[parInfAr.Length];
			for (var i = 0; i < parInfAr.Length; i++) {
				parTypAr[i] = parInfAr[i].ParameterType;
			}

			return baseMethod.DeclaringType.GetProperty(propertyInfo.Name, allProperties,
				null, propertyInfo.PropertyType, parTypAr, null);
		}

		public static FieldInfo GetFieldBaseDefinition(FieldInfo fieldInfo) {
			if (fieldInfo.DeclaringType == fieldInfo.ReflectedType) {
				return fieldInfo;
			}

			var fi = fieldInfo.DeclaringType.GetField(fieldInfo.Name, BindingFlags.Public | BindingFlags.Instance | BindingFlags.Static);
			if (fi == null) {
				throw new FatalException("pruser");
			}
			return fi;
		}


		private class GeneratedInstance {
			//all fields except marked as NoShow
			internal Dictionary<string, FieldInfo> fields = new Dictionary<string, FieldInfo>(StringComparer.OrdinalIgnoreCase);
			internal Dictionary<string, PropertyInfo> properties = new Dictionary<string, PropertyInfo>(StringComparer.OrdinalIgnoreCase);
			internal List<MethodInfo> buttonMethods = new List<MethodInfo>();

			//member-infos that can be inherited - we wont generate a new class for them but just use the one generated on a parental viewclass
			internal HashSet<MemberInfo> inheritedInfos = new HashSet<MemberInfo>();
			//names of generated classes for buttons/fields/properties associated to the respective members
			internal Dictionary<MemberInfo, string> ownInfos = new Dictionary<MemberInfo, string>();
			internal Dictionary<PropertyMethods, string> ownPropmeths = new Dictionary<PropertyMethods, string>(); //PropertyMethods from  descriptors in a simillar way...


			HashSet<DescriptorData> descriptors = new HashSet<DescriptorData>();//all descriptors will be here
			internal HashSet<DescriptorData> inheritedDescriptors = new HashSet<DescriptorData>();//DescriptorData that neednt be generated again (will be inherited)

			HashSet<string> nonGeneratedFields = new HashSet<string>();//set of fields that will be ommited during generating (filled from descriptors)

			internal Type type; //type of the viewable class
			string typeLabel; //label for info dialogs - obtained from ViewableClassAttribute
			CodeTypeDeclaration codeTypeDeclaration;

			string generatedClassName; //name of the generated class (source code)
			GeneratedInstance parentGI;

			internal GeneratedInstance(Type type) {
				this.type = type;
				var vcaArray = type.GetCustomAttributes(typeof(ViewableClassAttribute), false);
				ViewableClassAttribute vca = null;
				if (vcaArray.Length > 0) {
					//we have the ViewableClassAttribute, get it now
					vca = (ViewableClassAttribute) vcaArray[0];
				}
				//the vca needn't be present - if the type is kind of "viewableSpecial" then the class might be without this attribute...
				this.typeLabel = ((vca == null || vca.Name == null) ? type.Name : vca.Name); //label will be either the name of the type or specified label
				//binding flags for members of the actual type - do not consider members from the possible parent classes
				var forActualMembers = BindingFlags.IgnoreCase | BindingFlags.Instance | BindingFlags.Public | BindingFlags.DeclaredOnly;
				var forInheritedMembers = BindingFlags.IgnoreCase | BindingFlags.Instance | BindingFlags.Public;

				//now find any possible descriptors, we will have to add them too...
				var typeDescriptors = FindDescriptorsForType(type);
				foreach (var descriptorType in typeDescriptors) {
					//add a prepared DescriptorData class to the list
					var descData = new DescriptorData(descriptorType);
					this.descriptors.Add(descData);
					if (descData.nonDisplayedFields != null && descData.nonDisplayedFields.Length > 0) {
						foreach (var namedField in descData.nonDisplayedFields) {
							this.nonGeneratedFields.Add(namedField);
						}
					}
				}

				//get all fields from the actual Type except for those marked as "NoShow"
				var fldsMine = type.FindMembers(MemberTypes.Field | MemberTypes.Property, forActualMembers, HasntAttribute, typeof(NoShowAttribute));
				foreach (var fld in fldsMine) {
					var propinf = fld as PropertyInfo;
					//check also if it wasn't ommited by descriptor...
					if (propinf != null && !this.nonGeneratedFields.Contains(propinf.Name)) {
						this.properties.Add(propinf.Name, GetPropertyBaseDefinition(propinf));
					}
					var fldinf = fld as FieldInfo;
					//check also if it wasn't ommited by descriptor...
					if (fldinf != null && !this.nonGeneratedFields.Contains(fldinf.Name)) {
						this.fields.Add(fldinf.Name, GetFieldBaseDefinition(fldinf));
					}
				}
				//now get the fields from the whole hierarchy, but before adding them, check if they are not present in the set (which would mean
				//that we dont want to add it (this would overwrite the membere from the actual Type by the member from parent class with the same name)
				var fldsAll = type.FindMembers(MemberTypes.Field | MemberTypes.Property, forInheritedMembers, HasntAttribute, typeof(NoShowAttribute));
				foreach (var fld in fldsAll) {
					var propinf = fld as PropertyInfo;
					//check also if it wasn't ommited by descriptor...					
					if (propinf != null && !this.nonGeneratedFields.Contains(propinf.Name)) {
						if (!this.properties.ContainsKey(propinf.Name))//isnt this property already in the dictionary?
							this.properties.Add(propinf.Name, GetPropertyBaseDefinition(propinf));
					}
					var fldinf = fld as FieldInfo;
					//check also if it wasn't ommited by descriptor...					
					if (fldinf != null && !this.nonGeneratedFields.Contains(fldinf.Name)) {
						if (!this.fields.ContainsKey(fldinf.Name))//isnt this property already in the dictionary?
							this.fields.Add(fldinf.Name, GetFieldBaseDefinition(fldinf));
					}
				}

				//get all methods from the Type that have the "Button" attribute
				var mths = type.FindMembers(MemberTypes.Method, forInheritedMembers, HasAttribute, typeof(ButtonAttribute));
				foreach (var mi in mths) {
					var minf = mi as MethodInfo;
					if (minf != null) {
						this.buttonMethods.Add(MemberWrapper.GetMIBaseDefinition(minf));
					}
				}

				this.generatedClassName = "GeneratedDataView_" + this.type.Name + "_" + classCounter++;
				this.codeTypeDeclaration = new CodeTypeDeclaration(this.generatedClassName);
			}

			internal void SetHierarchy() {
				this.parentGI = this.GetParentGI();
				if (this.parentGI != null) {
					//first check descriptor data from parents
					foreach (var oneParentDesc in this.parentGI.descriptors) {
						if (this.descriptors.Contains(oneParentDesc)) { //descriptor data will be generated on parent
							this.inheritedDescriptors.Add(oneParentDesc);
						}
					}

					//remove all fields/properties/buttons that will be generated on parent (they needn't be duplicated)
					foreach (var parentsFieldKey in this.parentGI.fields.Keys) {
						if (this.fields.ContainsKey(parentsFieldKey)) { //field will be generated on parent
							this.inheritedInfos.Add(this.fields[parentsFieldKey]);
						}
					}
					foreach (var parentsPropertyKVP in this.parentGI.properties) {
						if (this.properties.ContainsKey(parentsPropertyKVP.Key)) { //property will be generated on parent
							//if both properties are declared on the same type then consider it as "inherited" (e.g. NPC does not contain Flag_Dead so this 
							//property comes from Character but since it exists on the Character (and will be generated there) we do not need to generate it again
							//on NPC
							if (parentsPropertyKVP.Value.DeclaringType == this.properties[parentsPropertyKVP.Key].DeclaringType) {
								//if (parentsPropertyKVP.Value.GetAccessors(true)[0].IsVirtual) {
								this.inheritedInfos.Add(this.properties[parentsPropertyKVP.Key]);
								//}
							}
						}
					}
					foreach (var parentsBtnMethod in this.parentGI.buttonMethods) {
						if (this.buttonMethods.Contains(parentsBtnMethod)) { //property will be generated on parent
							//if modifier 'new' is present then the Property should not be inherited
							//e.g. - NPCDef and the property TypeDef...
							if (parentsBtnMethod.IsVirtual) {
								this.inheritedInfos.Add(parentsBtnMethod);
							}
						}
					}
				}

				//now go through the list of descriptors and add all fields and buttons originating from the descriptor too...
				//we go through the descriptors first as we want their fields to be the first on the page
				foreach (var oneDesc in this.descriptors) {
					if (!this.inheritedDescriptors.Contains(oneDesc)) {
						//first, add classes for descriptor's buttons
						foreach (var buttonMethod in oneDesc.buttonMethods) {
							var oneButtonIDFW = oneDesc.GenerateButtonIDFW(buttonMethod);
							//buttonDataFieldViews.Add(oneButtonIDFW.Name); //store the name of the new generated type
							this.codeTypeDeclaration.Members.Add(oneButtonIDFW);
							this.ownInfos[buttonMethod] = oneButtonIDFW.Name; //store name of the newly generated class
						}

						//now the descriptor's fields...
						foreach (var oneKVP in oneDesc.describedFields) {
							var oneFieldIDFW = oneDesc.GenerateFieldIDFW(oneKVP.Value);
							//fieldsDataFieldViews.Add(oneFieldIDFW.Name); //store the name of the newly generated type. use the field name as key
							this.codeTypeDeclaration.Members.Add(oneFieldIDFW);
							this.ownPropmeths[oneKVP.Value] = oneFieldIDFW.Name; //store name of the newly generated class
						}
					}
				}

				//now create the inner classes of all own IDataFieldViews for this class
				//first classes for buttons
				foreach (var buttonMethod in this.buttonMethods) {
					if (!this.inheritedInfos.Contains(buttonMethod)) { //field not inherited, must be generated here
						var oneButtonIDFW = this.GenerateButtonIDFW(buttonMethod);
						//buttonDataFieldViews.Add(oneButtonIDFW.Name); //store the name of the new generated type
						this.codeTypeDeclaration.Members.Add(oneButtonIDFW);
						this.ownInfos[buttonMethod] = oneButtonIDFW.Name; //store name of the newly generated class
					}
				}
				//then classes for datafields
				foreach (var oneField in this.fields.Values) {
					if (!this.inheritedInfos.Contains(oneField)) { //field not inherited, must be generated here
						var oneFieldIDFW = this.GenerateFieldIDFW(oneField);
						//fieldsDataFieldViews.Add(oneFieldIDFW.Name); //store the name of the new generated type
						this.codeTypeDeclaration.Members.Add(oneFieldIDFW);
						this.ownInfos[oneField] = oneFieldIDFW.Name; //store name of the newly generated class
					}
				}
				foreach (var oneProperty in this.properties.Values) {
					if (!this.inheritedInfos.Contains(oneProperty)) { //field not inherited, must be generated here
						var onePropertyIDFW = this.GenerateFieldIDFW(oneProperty);
						//fieldsDataFieldViews.Add(onePropertyIDFW.Name); //store the name of the new generated type
						this.codeTypeDeclaration.Members.Add(onePropertyIDFW);
						this.ownInfos[oneProperty] = onePropertyIDFW.Name; //store name of the newly generated class
					}
				}
			}

			internal GeneratedInstance GetParentGI() {
				var nearestParent = this.type.BaseType;
				while (nearestParent != typeof(object)) {
					GeneratedInstance parentGI = null;
					if (generatedClasses.TryGetValue(nearestParent, out parentGI)) { //check if parental class has also a GeneratedInstance
						//direct parent has no generated view class, try another parent
						return parentGI;
					}
					nearestParent = nearestParent.BaseType;
				}
				return null;
			}

			#region Viewable class' DataView generating methods
			internal CodeTypeDeclaration GetGeneratedType() {
				this.codeTypeDeclaration.TypeAttributes = TypeAttributes.Public;
				if (this.parentGI == null) {
					this.codeTypeDeclaration.BaseTypes.Add(typeof(ViewableClassDataView)); //default parent class name
				} else {
					this.codeTypeDeclaration.BaseTypes.Add(this.parentGI.generatedClassName); //inheritance between generated classes
				}
				this.codeTypeDeclaration.IsClass = true;

				//first add two methods for getting the ActionsButtonsPage and DataFieldsPage
				this.codeTypeDeclaration.Members.Add(this.GenerateGetActionButtonsPageMethod());
				this.codeTypeDeclaration.Members.Add(this.GenerateGetDataFieldsPageMethod());

				this.codeTypeDeclaration.Members.Add(this.GenerateGetActionButtonsCountMethod());
				this.codeTypeDeclaration.Members.Add(this.GenerateGetFieldsCountMethod());
				this.codeTypeDeclaration.Members.Add(this.GenerateGetNameMethod());
				this.codeTypeDeclaration.Members.Add(this.GenerateHandledTypeProperty());

				//preapre the fields and actionbuttons storages
				var buttonDataFieldViews = new List<string>();
				var fieldsDataFieldViews = new List<string>();

				//now go through the list of descriptors and add all fields and buttons originating from the descriptor too...
				//we go through the descriptors first as we want their fields to be the first on the page
				foreach (var oneDesc in this.descriptors) {
					if (!this.inheritedDescriptors.Contains(oneDesc)) {
						//first, add classes for descriptor's buttons
						foreach (var buttonMethod in oneDesc.buttonMethods) {
							buttonDataFieldViews.Add(this.ownInfos[buttonMethod]); //store the name of the new generated type
						}

						//now the descriptor's fields...
						foreach (var oneKVP in oneDesc.describedFields) {
							fieldsDataFieldViews.Add(this.ownPropmeths[oneKVP.Value]); //store the name of the new generated type
						}
					} else { //descriptor is inherited from parent - use the parent's descriptor info
						//first, add classes for descriptor's buttons
						foreach (var buttonMethod in oneDesc.buttonMethods) {
							var gi = this.parentGI;
							while (true) {
								string name;
								if (gi.ownInfos.TryGetValue(buttonMethod, out name)) {
									buttonDataFieldViews.Add(name);//if the field is also on parent, store the name from the parent's field
									break;
								}
								gi = gi.parentGI;
							}
						}

						//now the descriptor's fields...
						foreach (var oneKVP in oneDesc.describedFields) {
							var gi = this.parentGI;
							while (true) {
								string name;
								if (gi.ownPropmeths.TryGetValue(oneKVP.Value, out name)) {
									fieldsDataFieldViews.Add(name);//if the field is also on parent, store the name from the parent's field
									break;
								}
								gi = gi.parentGI;
							}
						}
					}
				}

				//first classes for buttons
				foreach (var buttonMethod in this.buttonMethods) {
					if (!this.inheritedInfos.Contains(buttonMethod)) { //field not inherited, must be generated here
						buttonDataFieldViews.Add(this.ownInfos[buttonMethod]); //store the name of the new generated type
					} else {
						var gi = this.parentGI;
						while (true) {
							string name;
							if (gi.ownInfos.TryGetValue(buttonMethod, out name)) {
								buttonDataFieldViews.Add(name);//if the field is also on parent, store the name from the parent's field
								break;
							}
							gi = gi.parentGI;
						}
					}
				}
				//then classes for datafields
				foreach (var oneField in this.fields.Values) {
					if (!this.inheritedInfos.Contains(oneField)) { //field not inherited, must be generated here
						fieldsDataFieldViews.Add(this.ownInfos[oneField]); //store the name of the new generated type
					} else {
						var gi = this.parentGI;
						while (true) {
							string name;
							if (gi.ownInfos.TryGetValue(oneField, out name)) {
								fieldsDataFieldViews.Add(name);//if the field is also on parent, store the name from the parent's field
								break;
							}
							gi = gi.parentGI;
						}
					}
				}

				foreach (var oneProperty in this.properties.Values) {
					if (!this.inheritedInfos.Contains(oneProperty)) { //field not inherited, must be generated here
						fieldsDataFieldViews.Add(this.ownInfos[oneProperty]); //store the name of the new generated type
					} else {
						var gi = this.parentGI;
						while (true) {//can happen that there is no parent left ...
							string name;
							if (gi.ownInfos.TryGetValue(oneProperty, out name)) {
								fieldsDataFieldViews.Add(name);//if the field is also on parent, store the name from the parent's field
								break;
							}
							gi = gi.parentGI;
						}
					}
				}

				//now add a Class representing one Page of action buttons
				this.codeTypeDeclaration.Members.Add(this.GenerateActionButtonsPage(buttonDataFieldViews));
				//and a Class representing one Page of other datafields
				this.codeTypeDeclaration.Members.Add(this.GenerateDataFieldsPage(fieldsDataFieldViews));

				return this.codeTypeDeclaration;
			}

			/// <summary>Method for getting one page of action buttons</summary>
			private CodeMemberMethod GenerateGetActionButtonsPageMethod() {
				var retVal = new CodeMemberMethod();
				retVal.Attributes = MemberAttributes.Public | MemberAttributes.Override;
				retVal.Name = "GetActionButtonsPage";
				retVal.Parameters.Add(new CodeParameterDeclarationExpression(typeof(int), "firstLineIndex"));
				retVal.Parameters.Add(new CodeParameterDeclarationExpression(typeof(object), "target"));
				retVal.ReturnType = new CodeTypeReference(typeof(IEnumerable<ButtonDataFieldView>));

				retVal.Statements.Add(new CodeMethodReturnStatement(
					//makes the "new ..." section
										new CodeObjectCreateExpression(this.type.Name + "ActionButtonsPage",
					//two parameters
											new CodeVariableReferenceExpression("firstLineIndex"),
											new CodeVariableReferenceExpression("target"))
										));
				return retVal;
			}

			/// <summary>Method for getting one page of data fields buttons</summary>
			private CodeMemberMethod GenerateGetDataFieldsPageMethod() {
				var retVal = new CodeMemberMethod();
				retVal.Attributes = MemberAttributes.Public | MemberAttributes.Override;
				retVal.Name = "GetDataFieldsPage";
				retVal.Parameters.Add(new CodeParameterDeclarationExpression(typeof(int), "firstLineIndex"));
				retVal.Parameters.Add(new CodeParameterDeclarationExpression(typeof(object), "target"));
				retVal.ReturnType = new CodeTypeReference(typeof(IEnumerable<IDataFieldView>));

				retVal.Statements.Add(new CodeMethodReturnStatement(
										new CodeObjectCreateExpression(this.type.Name + "DataFieldsPage",
											new CodeVariableReferenceExpression("firstLineIndex"),
											new CodeVariableReferenceExpression("target"))
										));
				return retVal;
			}

			/// <summary>The GetActionButtonsCount method - says how many buttons will there be</summary>
			private CodeMemberMethod GenerateGetActionButtonsCountMethod() {
				var buttonsCount = 0;
				buttonsCount += this.buttonMethods.Count; //class' own buttons
				foreach (var desc in this.descriptors) {
					buttonsCount += desc.buttonMethods.Count; //plus descriptor's buttons...
				}

				var retVal = new CodeMemberMethod();
				retVal.Attributes = MemberAttributes.Public | MemberAttributes.Override;
				retVal.Name = "GetActionButtonsCount";
				retVal.Parameters.Add(new CodeParameterDeclarationExpression(typeof(object), "instance"));
				retVal.ReturnType = new CodeTypeReference(typeof(int));
				retVal.Statements.Add(new CodeMethodReturnStatement(
											new CodePrimitiveExpression(
												buttonsCount
										)));
				return retVal;
			}

