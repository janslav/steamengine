	internal sealed class ViewableClassGenerator : ISteamCsCodeGenerator {
		//array of special types which (or whose children) will be viewable even if the classes doesn't have the
		//viewable attribute
		static Type[] viewableSpecials = { typeof(Plugin) };

		static List<Type> viewableClasses = new List<Type>();

		//key is type, value is the list of its descriptors
		static Dictionary<Type, List<Type>> viewableDescriptorsForTypes = new Dictionary<Type, List<Type>>();

		//generated instances associated with their real Types - used for hierarchic generating
		static Dictionary<Type, GeneratedInstance> generatedClasses = new Dictionary<Type, GeneratedInstance>();

		internal static int classCounter;

		public CodeCompileUnit WriteSources() {
			try {
				var codeCompileUnit = new CodeCompileUnit();
				if (viewableClasses.Count > 0) {
					Logger.WriteDebug("Generating DataViews and View Descriptors");

					var ns = new CodeNamespace("SteamEngine.CompiledScripts.Dialogs");
					codeCompileUnit.Namespaces.Add(ns);

					//first prepare the GIs for generating - find all fields,properties,buttons to be generated
					foreach (var viewableClass in viewableClasses) {
						try {
							//class for generating all fields, including those from possible descriptors
							var gi = new GeneratedInstance(viewableClass);
							generatedClasses[viewableClass] = gi; //store the generated instance for this type
						} catch (FatalException) {
							throw;
						} catch (TransException) {
							throw;
						} catch (Exception e) {
							Logger.WriteError(viewableClass.Assembly.GetName().Name, viewableClass.Name, e);
							return null;
						}
					}

					//now remove unnecessary fields/properties/buttons being generated by parents and prepare inner classes
					foreach (var oneGI in generatedClasses.Values) {
						oneGI.SetHierarchy();
					}

					//finally generate all classes
					foreach (var oneGI in generatedClasses.Values) {
						try {
							//class for generating all fields, including those from possible descriptors
							if (oneGI.GetAllFieldViewsCount() > 0) {//we have at least one MemberInfo
								var ctd = oneGI.GetGeneratedType();
								ns.Types.Add(ctd);
							}
						} catch (FatalException) {
							throw;
						} catch (TransException) {
							throw;
						} catch (Exception e) {
							Logger.WriteError(oneGI.type.Assembly.GetName().Name, oneGI.type.Name, e);
							return null;
						}
					}
					Logger.WriteDebug("Done generating " + viewableClasses.Count + " DataViews and View Descriptors");
				}
				return codeCompileUnit;
			} finally {
				viewableClasses.Clear();
			}
		}

