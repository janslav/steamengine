				var forMembers = BindingFlags.IgnoreCase | BindingFlags.Static | BindingFlags.Public;

				//get all methods from the descriptor that have the "Button" attribute
				var mths = descriptorType.FindMembers(MemberTypes.Method, forMembers, HasAttribute, typeof(ButtonAttribute));
